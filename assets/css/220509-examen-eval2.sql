	codCiudad VARCHAR2(3),
	nombre VARCHAR2(50),
	pais VARCHAR2(2),
	tamanno INTEGER,
	CONSTRAINT pk_ciudad PRIMARY KEY (codCiudad),
	CONSTRAINT fk_pais FOREIGN KEY (pais) REFERENCES pais(codPais)
);
